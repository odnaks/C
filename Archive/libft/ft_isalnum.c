int		ft_isalnum(int c)
{
	if (((int)c >= 'a' && (int)c <= 'z') ||
		((int)c >= 'A' && (int)c <= 'Z') ||
		((int)c >= '0' && (int)c <= '9'))
		return (1);
	else
		return (0);
}

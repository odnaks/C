#include "libft.h"

int	ft_atoi(const char *s)
{
	int	flag;
	int	n;
	int	des;

	n = 0;
	des = 10;
	while (*s == ' ' || *s == '\t' || *s == '\n' ||
			*s == '\v' || *s == '\f' || *s == '\r')
		s++;
	flag = 1;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			flag = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		n = n * 10 + (*s - '0');
		s++;
	}
	return (n * flag);
}

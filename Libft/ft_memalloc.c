#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*a;

	if (size == 0)
		return (NULL);
	a = (void*)malloc(size);
	if (a)
	{
		ft_bzero(a, size);
		return (a);
	}
	return (NULL);
}

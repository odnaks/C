#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t num)
{
	char *s;

	s = s1;
	while (*s != '\0')
		s++;
	while (*s2 != '\0' && (num > 0))
	{
		*s++ = *s2++;
		num--;
	}
	*s = '\0';
	return (s1);
}
